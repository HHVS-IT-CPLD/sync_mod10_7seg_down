// ============================================================================
// 模組名稱：sync_mod10_7seg_down
// 功能說明：
//   1) 以 CLK 為時脈，實作「同步除 10 下數計數器」。
//   2) 計數序列為：9 -> 8 -> ... -> 0 -> 9 -> ... 持續循環。
//   3) 將計數值即時解碼到共陽極 (Common Anode) 七段顯示器。
//
// 目標裝置：MAX II EPM1270T144C5
// 建議腳位連接（請於 Quartus Pin Planner / .qsf 完成實際綁定）：
//   - 七段顯示器 a~g：CPLD Pin 1~7
//   - CLK：CPLD Pin 11
//   - RST：CPLD Pin 12
//
// 重要：
//   - 本設計假設七段顯示器為「共陽極」，因此段信號為「低電位點亮」。
//   - seg[6:0] 對應順序固定為 {a,b,c,d,e,f,g}。
// ============================================================================
module sync_mod10_7seg_down (
	input  wire       CLK,   // 系統時脈輸入
	input  wire       RST,   // 同步重置信號（高有效）
	output reg  [6:0] seg    // 七段輸出：{a,b,c,d,e,f,g}，共陽極（0=亮，1=滅）
);

	// ------------------------------------------------------------------------
	// 計數器暫存器
	// ------------------------------------------------------------------------
	// 只需表示 0~9，因此使用 4-bit 即可。
	reg [3:0] count;

	// ------------------------------------------------------------------------
	// 同步下數計數邏輯
	// ------------------------------------------------------------------------
	// 在每個 CLK 上升沿更新 count：
	//   - RST=1 時，計數器被同步清到 9（顯示起始值為 9）
	//   - 否則每拍減 1
	//   - 減到 0 之後，下拍回到 9，形成除 10 循環
	always @(posedge CLK) begin
		if (RST) begin
			count <= 4'd9;
		end else begin
			if (count == 4'd0)
				count <= 4'd9;
			else
				count <= count - 4'd1;
		end
	end

	// ------------------------------------------------------------------------
	// BCD(0~9) -> 七段顯示器解碼（共陽極）
	// ------------------------------------------------------------------------
	// bit 對應：seg = {a,b,c,d,e,f,g}
	// 共陽極規則：
	//   - 0：該段點亮
	//   - 1：該段熄滅
	//
	// 範例：
	//   顯示 0 時，a~f 亮、g 滅 => 7'b0000001
	// ------------------------------------------------------------------------
	always @(*) begin
		case (count)
			4'd0: seg = 7'b0000001;
			4'd1: seg = 7'b1001111;
			4'd2: seg = 7'b0010010;
			4'd3: seg = 7'b0000110;
			4'd4: seg = 7'b1001100;
			4'd5: seg = 7'b0100100;
			4'd6: seg = 7'b0100000;
			4'd7: seg = 7'b0001111;
			4'd8: seg = 7'b0000000;
			4'd9: seg = 7'b0000100;

			// 保底狀態：若 count 非 0~9，顯示全滅避免錯誤圖形
			default: seg = 7'b1111111;
		endcase
	end

endmodule